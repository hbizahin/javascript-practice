// switch is more faster than if else
color = 'yellow';

switch(color){
    case 'green':
        console.log('you are green');
        break;
    case 'red':
        console.log('you are red');
        break;
    case 'yellow':
        console.log('you are yellow');
        break;
    default:
        console.log('you are Kala Kala')
}

